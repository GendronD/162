void Game_Simulation::simulation()
{
    SirCodesAlot = new Character();
    game_events = new Event(SirCodesAlot);

    forest = new Forest(game_events);
    glade = new Glade(game_events);
    cottage = new Cottage(game_events);
    dirt_path = new Dirt_Path(game_events);
    field = new Field(game_events);
    ridge = new Ridge(game_events);
    rift_gate = new Rift_Gate(game_events);
    mountain_pass = new Mountain_Pass(game_events);
    lair = new Lair(game_events);

    //Allocate Maps
    //Argument Order:           Top,       Bottom,        Right,   Left,          Location,        Player
    forest->newLocation        (nullptr,   nullptr,       glade,   nullptr,       "Forest",        SirCodesAlot);
    glade->newLocation         (nullptr,   nullptr,       cottage, forest,        "Glade",         SirCodesAlot);
    cottage->newLocation       (dirt_path, rift_gate,     ridge,   glade,         "Cottage",       SirCodesAlot);
    dirt_path->newLocation     (nullptr,   cottage,       field,   nullptr,       "Dirt Path",     SirCodesAlot);
    field->newLocation         (nullptr,   ridge,         nullptr, dirt_path,     "Field",         SirCodesAlot);
    ridge->newLocation         (field,     nullptr,       nullptr, cottage,       "Ridge",         SirCodesAlot);
    rift_gate->newLocation     (cottage,   mountain_pass, nullptr, nullptr,       "Rift Gate",     SirCodesAlot);
    mountain_pass->newLocation (rift_gate, nullptr,       lair,    nullptr,       "Mountain Pass", SirCodesAlot);
    lair->newLocation          (nullptr,   nullptr,       nullptr, mountain_pass, "Lair",          SirCodesAlot);


    int game_over = 0;
    int user_choice = 0;
    int start = 0;
    int steps = 12;

    SirCodesAlot->setCurrentLocation(forest);

    std::cout << "Find and defeat the Evil SegFault to join the Knights of the Code Table." << std::endl;
    std::cout << "Beware! You only have 12 steps, so use them wisely!" << std::endl;

    do
    {
        std::cout << "+------------------------+" << std::endl;
        std::cout << "| Current Steps Left: " << steps << " |" << std::endl;
        std::cout << "+------------------------+" << std::endl;

        //Run first event
        if (start == 0)
        {
            SirCodesAlot->getCurrentLocation()->item_event();
            SirCodesAlot->getCurrentLocation()->dialogue_event();
            SirCodesAlot->getCurrentLocation()->action_event();
            start++;
        }

        //print map
        map(SirCodesAlot);

        //movement choices
        user_choice = char_choice();
        switch(user_choice)
        {
            case 1 : //move top
                SirCodesAlot->travel_top();
                steps--;
                break;
            case 2 : //move bottom
                SirCodesAlot->travel_bottom();
                steps--;
                break;
            case 3 : //move right
                SirCodesAlot->travel_right();
                steps--;
                break;
            case 4 :  //move left
                SirCodesAlot->travel_left();
                steps--;
                break;
            default :
                break;
        }

        //print map for last node
        if (SirCodesAlot->getCurrentLocation() == lair)
        {
            map(SirCodesAlot);
        }

        //Run Location Events
        SirCodesAlot->getCurrentLocation()->item_event();
        SirCodesAlot->getCurrentLocation()->dialogue_event();
        SirCodesAlot->getCurrentLocation()->action_event();

        //game must end victory or loss after lair
        if (SirCodesAlot->getCurrentLocation() == lair)
        {
            game_over = 1;
        }

        //if user runs out of steps
        if (steps <= 0)
        {
            game_over = 1;
            std::cout << "**GAME OVER**" << std::endl;
            std::cout << "You ran out of steps!" << std::endl;
        }

    }while(game_over != 1);

    // free up heap
    delete SirCodesAlot;
    delete game_events;
    delete forest;
    delete glade;
    delete cottage;
    delete dirt_path;
    delete field;
    delete ridge;
    delete rift_gate;
    delete mountain_pass;
    delete lair;

    SirCodesAlot = nullptr;
    game_events = nullptr;
    forest = nullptr;
    glade = nullptr;
    cottage = nullptr;
    dirt_path = nullptr;
    field = nullptr;
    ridge = nullptr;
    rift_gate = nullptr;
    mountain_pass = nullptr;
    lair = nullptr;
    /*delete SirCodesAlot;
    SirCodesAlot = nullptr;
    delete forest;
    forest = nullptr;*/
}